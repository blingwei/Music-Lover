        <div class="topic-richtext">
          <p>马上过年了，看到好多豆油因为猫咪携带问题焦头烂额。刚好我家有过多次携带进客舱的经验，开个贴帮大家答疑解惑。</p><p>收费标准：800/航段/每只宠物</p><p>商务舱不可携带宠物</p><p>运输条件</p><p>1. 具备进客舱条件的城市包括：海口、广州、深圳、长沙、济南、青岛、厦门、合肥、喀什、太原、西安、哈尔滨、武汉、郑州、桂林、上海虹桥、大连、南宁。仅适用于直飞。只要求出港城市在以上城市之内，落地城市不作要求。（出发地不在以上城市或者没有海航直飞航班的朋友可以关贴了😂</p><p>2. 宠物（只适用于猫狗）+宠物箱+衣服嘴套尿不湿（进客舱必备）的重量不能超过5kg（猫咪自重超过4kg的基本都会超）</p><p>3. 每个航班最多可带两只宠物，每人最多可带一只。</p><p>4. 单独乘机的未成年人、孕妇、儿童、七十岁以上老人、病残，单独带婴儿或儿童的成年人不可办理进客舱</p><p>5. 缅甸猫、喜马拉雅猫、波斯猫、异国短毛猫不可办理进客舱</p><p>6. 出生不足六个月、怀孕、起飞前48小时刚分娩过的猫咪不可办理进客舱</p><p>7. 患有耳鼻喉科、心血管系统、脑血管系统、呼吸系统、消化系统疾病，以及48小时内进行过手术的宠物不可办理进客舱</p><p>8. 服用镇静剂或安眠药的宠物不可办理进客舱</p><p>9. 浑身散发恶臭或让人难以忍受的刺鼻气味的宠物不可办理进客舱</p><p>10. 性格焦躁、娇气、弱小，对高温高空环境敏感或不能长时间呆在宠物箱里的宠物不可办理进客舱</p><p>符合以上条件后正式开始办理</p><p>运输材料</p><p>官方所需材料：</p><p>1. 动物卫生监督所出具的《动物检疫合格证明》，其中“运载工具消毒情况”一栏必须填写且单据上需盖有动物卫生监督所检疫专用章。（视情况自行办理or🍑宝买）</p><p>2. 宠物疫苗注射证明。（包括狂犬疫苗。如果自己家里的疫苗本注射日期在一年内的可以直接拿去用，超过一年又不想再次注射的可以找相熟的宠物医院或者🍑宝做一个fake的，注意注射日期一定要标注哦）</p><p>3. 运输协议书：旅客阅读并填写《客舱运输宠物协议书（请点击此处查看）》（协议书可以自行准备，也可以去柜台申请运输的时候叫工作人员打印。注意⚠️打印一式三份，自留一份，柜台申请一份，值机柜台办托运一份）</p><p>⚠️材料准备好后至少提前24小时预约宠物进客舱。预约渠道：</p><p>（1）航班出港地的海南航空直属售票处；</p><p>（2）航班出港地的海南航空授权的售票代理人。</p><p>自备材料：</p><p>1. 宠物箱：长*宽*高尺寸不得超过 35*28*24厘米，必须是软箱且有效防止渗透，不易被咬开（我的经验是长宽高超过一点点可以接受，工作人员不会严格到拿着皮尺去测量。但是不能超太多，一定是要能放在前排座椅下方的尺寸）</p><p>2. 宠物衣服</p><p>3. 宠物嘴套（我的经验是买个最小的狗嘴套，因为确实没有猫咪专用的嘴套，能戴上不掉就行）</p><p>4. 宠物尿不湿</p><p>以上都准备好之后就可以愉快地带着孩子一起坐灰机啦</p><p>乘机过程</p><p>严格走程序的话猫咪会被检查三次（柜台、安检、登机）</p><p>正常情况会有海航专业地陪帮助完成带猫进客舱的手续，一直送人和动物到安检。如果没有，咱也不能怎么办😂</p><p>1. 到达机场（好的这是一个废话步骤）</p><p>2. 办理宠物进客舱手续。宠物进客舱会有专门的柜台办理，一般是特殊旅客柜台，走过去彬彬有礼理直气壮地告诉他你办宠物进客舱。然后就开始一系列手续，检查三种材料，检查猫包猫咪情况（衣服嘴套尿不湿），开猫咪机票（名目其实就是个超重行李🙄️），交钱，走人。⚠️座位安排一定是最后一排靠窗的位置，记得检查好机票</p><p>3. 安检。普通旅客通道or特殊通道不确定，看工作人员安排，可以争取一下特殊通道。进安检会先检查三种材料，然后猫走行李安检（可以争取一下问工作人员能不能抱着猫过安检，我争取到过一次，毕竟行李检查辐射大，能少一次是一次）</p><p>4. 登机。正常来讲带宠物是优先登机的，如果工作人员没有这么操作咱也没办法，值机柜台或者机舱内会检查三种材料和猫咪，然后就顺利登机啦～</p><p>个人经验</p><p>怕猫咪焦虑吵闹可以随身准备加巴喷丁，提前2小时给猫咪服用</p><p>到机场多问多提要求。比如地陪，比如抱猫过安检，比如优先进客舱。因为宠物进客舱这个业务海航没有专门培训过，导致很多城市操作不规范（有利有弊）缺少很多服务，所以多问不会有错的。不强求服务，有的话更好对不😎</p><p>衣服口套尿不湿视情况穿戴，如果孩子不太配合的话就提前穿戴。我家是到检查时才穿，能多舒服一会儿是一会儿。上飞机之后其实就可以都偷偷摘掉了，遇到机舱人少且空乘喜欢小动物，甚至可以正大光明放出来玩（⚠️记得戴肩背牵引绳）</p><div class="image-container image-float-center"><div class="image-wrapper"><img src="https://img1.doubanio.com/view/group_topic/l/public/p232238488.webp" width="500"></div></div><p>一脸见过世面</p><div class="image-container image-float-center"><div class="image-wrapper"><img src="https://img1.doubanio.com/view/group_topic/l/public/p232238498.webp" width="500"></div></div><p>等下飞机等到昏迷</p>
